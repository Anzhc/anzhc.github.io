import { access, mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const profileConfigs = [
  {
    id: 'anzhc',
    title: 'Anzhc',
    profileHref: 'https://huggingface.co/Anzhc',
    moreHref: 'https://huggingface.co/Anzhc/models',
    fetchMode: 'top',
    featuredCount: 2
  },
  {
    id: 'cabal-research',
    title: 'Cabal Research',
    profileHref: 'https://huggingface.co/CabalResearch',
    moreHref: 'https://huggingface.co/CabalResearch/models',
    fetchMode: 'pinned',
    pinnedRepoId: 'CabalResearch/NoobAI-Flux2VAE-RectifiedFlow-0.3'
  },
  {
    id: 'chenkin-rf',
    title: 'ChenkinRF',
    profileHref: 'https://huggingface.co/ChenkinRF',
    moreHref: 'https://huggingface.co/ChenkinRF/models',
    fetchMode: 'top',
    featuredCount: 2
  }
];

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const outputDir = path.join(repoRoot, 'src', 'content');
const outputPath = path.join(repoRoot, 'src', 'content', 'projects.ts');

const writeProjectsFile = async (projectsContent) => {
  await mkdir(outputDir, { recursive: true });

  const fileContents =
    "import type { ProjectsContent } from './types';\n\n" +
    '// This file is auto-generated by scripts/generate-huggingface-projects.mjs.\n' +
    `export const projects: ProjectsContent = ${JSON.stringify(projectsContent, null, 2)};\n`;

  await writeFile(outputPath, fileContents, 'utf8');
};

const fetchJson = async (url) => {
  const response = await fetch(url, {
    headers: {
      Accept: 'application/json'
    }
  });

  if (!response.ok) {
    throw new Error(`Hugging Face API returned ${response.status}`);
  }

  return response.json();
};

const normalizeModel = (model) => {
  const repoId = String(model.id);
  const repoName = repoId.split('/').at(-1) ?? repoId;

  return {
    id: repoId,
    name: repoName,
    repoPath: repoId,
    href: `https://huggingface.co/${repoId}`,
    monthlyDownloads: Number(model.downloads ?? 0)
  };
};

const fetchTopPanel = async (config) => {
  const author = new URL(config.profileHref).pathname.slice(1);
  const apiUrl =
    `https://huggingface.co/api/models?author=${author}` +
    '&sort=downloads&direction=-1&limit=100&expand[]=downloads&expand[]=downloadsAllTime';
  const models = await fetchJson(apiUrl);

  return {
    id: config.id,
    title: config.title,
    sourceHref: config.profileHref,
    moreCount: Math.max(models.length - config.featuredCount, 0),
    moreHref: config.moreHref,
    items: models.slice(0, config.featuredCount).map(normalizeModel)
  };
};

const fetchPinnedPanel = async (config) => {
  const model = await fetchJson(`https://huggingface.co/api/models/${config.pinnedRepoId}`);

  return {
    id: config.id,
    title: config.title,
    sourceHref: config.profileHref,
    moreCount: 0,
    moreHref: config.moreHref,
    items: [normalizeModel(model)]
  };
};

const fetchProjects = async () => {
  const panels = await Promise.all(
    profileConfigs.map((config) =>
      config.fetchMode === 'pinned' ? fetchPinnedPanel(config) : fetchTopPanel(config)
    )
  );

  return {
    clusterTitle: 'Personal',
    generatedAt: new Date().toISOString(),
    panels
  };
};

try {
  const projectsContent = await fetchProjects();
  await writeProjectsFile(projectsContent);
  console.log('Updated static Hugging Face projects.');
} catch (error) {
  try {
    await access(outputPath);
    const existingFile = await readFile(outputPath, 'utf8');

    console.warn(
      'Failed to refresh Hugging Face projects, keeping the existing static snapshot.\n' +
        String(error)
    );

    if (!existingFile.trim()) {
      throw error;
    }
  } catch {
    throw error;
  }
}
